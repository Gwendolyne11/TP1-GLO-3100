def aes_sbox_lookup(input_value):
    row = (input_value >> 4) & 0x0F
    col = input_value & 0x0F
    return AES_S_BOX[row][col]


def walsh_hadamard_transform(sbox):
    n = len(sbox)
    transform = np.zeros(n, dtype=int)

    for i in range(n):
        for j in range(n):
            input_bits = np.array(list(map(int, list(f"{j:08b}"))))
            linear_combination = (
                np.dot(input_bits, list(map(int, list(f"{i:08b}")))) % 2
            )
            output_bit = sbox[j]
            transform[i] += (-1) ** (linear_combination ^ output_bit)

    return transform


def max_theoretical_non_linearity(n_bits):
    return (2 ** (n_bits - 1)) - (2 ** ((n_bits // 2) - 1))


def non_linearity(sbox):
    transform = walsh_hadamard_transform(sbox)
    max_walsh_hadamard_value = max(abs(transform))
    non_linearity_value = (len(sbox) // 2) - (max_walsh_hadamard_value // 2)

    return non_linearity_value


